package modulo2;

public class OperadoresAsignacion {
    public static void main(String[] args) {
        int a = 5; int b = 4;
        a += b;
        System.out.println(a);
        a -= b;
        System.out.println(a);
        a *= b;
        System.out.println(a);
        a /= 5;
        System.out.println(a);

    }
}
